#!/usr/bin/env python3
"""
This module provides a function for finding the floor of a float and types it
"""
import math


def floor(n: float) -> int:
    """
    This function finds the floor of a float and returns the result as an
    integer.

    Parameters:
    n (float): The float to find the floor of.

    Returns:
    int: The floor of n.
    """
    return math.floor(n)
